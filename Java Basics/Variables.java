
public class Variables {

	public static void main(String[] args) {

		int a=10;
		int b= 20;
		
		int c = a+b;
		System.out.println("Sum: "+c);
		
		int d= a*b;
		System.out.println("Multiplication: "+d);
		
		c= a*b; 				//re-initialization
		System.out.println("Multiplication: "+c);
		
		
	}

}
